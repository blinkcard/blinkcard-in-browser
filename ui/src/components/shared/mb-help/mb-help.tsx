import {
  h,
  Component,
  Host,
  FunctionalComponent,
  State,
  Prop,
  Method
} from "@stencil/core";
import { classNames } from "../../../utils/generic.helpers";
import { TranslationService } from "../../../utils/translation.service";
import {
  DEFAULT_MB_HELP_TOOLTOP_PAUSE_TIMEOUT_MS,
  DEFEAULT_MB_HELP_ALLOW,
  DEFEAULT_MB_HELP_ALLOW_LOBBY,
  DEFEAULT_MB_HELP_ALLOW_ONBOARDING,
  DEFEAULT_MB_HELP_ALLOW_ONBOARDING_PERPETUITY,
  MbHelpCallbacks
} from "./mb-help.model";

@Component({
  tag: 'mb-help',
  styleUrl: 'mb-help.scss',
  shadow: true
})
export class MbHelp {
  private readonly ONBOARDING_OPEN_DELAY_MS = 100;
  private texts: ComponentTexts;
  private onboardingOpenCount: number;
  private tooltipTimerId: number;
  private callbacks: MbHelpCallbacks | null;
  @State() isInitialized: boolean;
  @State() isTooltipShownHover: boolean;
  @State() isTooltipShownTimer: boolean;
  @State() isModalShown: boolean;
  @State() activeStepIndex: number;

  /**
   * Dictates if usage is allowed (turned on).
   */
  @Prop() allow: boolean = DEFEAULT_MB_HELP_ALLOW;
  /**
   * Dictates if Floating-Action-Button (Fab) is shown.
   */
  @Prop() allowFab: boolean = DEFEAULT_MB_HELP_ALLOW_LOBBY;
  /**
   * Dictates if the onboarding is allowed.
   */
  @Prop() allowOnboarding: boolean = DEFEAULT_MB_HELP_ALLOW_ONBOARDING;
  /**
   * Dictates if onboarding is executed all the time, or just once.
   */
  @Prop() allowOnboardingPerpetuity: boolean = DEFEAULT_MB_HELP_ALLOW_ONBOARDING_PERPETUITY;
  /**
   * Dictates Milliseconds after which a "Need Help?" tooltip will be shown.
   */
  @Prop() tooltipPauseTimeout: number = DEFAULT_MB_HELP_TOOLTOP_PAUSE_TIMEOUT_MS;
  /**
   * Translation service.
   */
  @Prop() translationService!: TranslationService;
  /**
   * Initializes - starts tooltip timer, etc.
   */
  @Method() async initialize(callbacks: MbHelpCallbacks) {
    if (!this.allow) { return; }
    this.startTooltipTimer();
    this.callbacks = callbacks;
    this.isInitialized = true;
  }
  /**
   * Opens modal for Onboarding purpose.
   */
  @Method() async openOnboarding() {
    if(!this.allow || !this.allowOnboarding) { return; }
    if(!this.allowOnboardingPerpetuity && this.onboardingOpenCount > 0) { return; }
    this.activeStepIndex = 0;
    this.openInternal(this.ONBOARDING_OPEN_DELAY_MS);
    this.onboardingOpenCount++;
  }
  /**
   * Opens modal for Help Screens purpose.
   */
  @Method() async openHelpScreens() {
    if (!this.allow) { return; }
    this.activeStepIndex = 1;
    this.openInternal();
  }
  /**
   * Closes modal.
   */
  @Method() async close() {
    if (!this.allow) { return; }
    this.closeInternal();
  }
  /**
   * Terminates - cancels tooltip timer, closes modal, etc.
   */
  @Method() async terminate() {
    this.callbacks = null; // has to be before closeInternal
    this.closeInternal(true);
    this.cancelTooltipTimer();
    this.isInitialized = false;
  }

  constructor() {
    this.isInitialized = false;
    this.isTooltipShownHover = false;
    this.isTooltipShownTimer = false;
    this.onboardingOpenCount = 0;
    this.closeInternal(true);
    this.cancelTooltipTimer();
    this.prepareTexts();
  }

  private openInternal = (withDelayMs = 0) => {
    setTimeout(() => {
      this.cancelTooltipTimer();
      this.isTooltipShownTimer = false;
      this.isModalShown = true;
      this.callbacks?.onOpen();
    }, withDelayMs);
  };

  private closeInternal = (skipTooltipTimerStart = false) => {
    this.isModalShown = false;
    this.activeStepIndex = 1;
    if (!skipTooltipTimerStart) {
      this.startTooltipTimer();
    }
    this.callbacks?.onClose();
  };

  private startTooltipTimer = () => {
    this.isTooltipShownTimer = false;
    const myTooltipTimerId = Date.now();
    this.tooltipTimerId = myTooltipTimerId;
    setTimeout(
      () => {
        if (this.tooltipTimerId !== myTooltipTimerId) { return; }
        this.isTooltipShownTimer = true;
      },
      this.tooltipPauseTimeout
    )
  };

  private cancelTooltipTimer = () => {
    this.tooltipTimerId = null;
  };

  private amIVisible = (myVisibility?: boolean) => {
    return this.isInitialized && this.allow && (myVisibility == null ? true : myVisibility);
  }

  render() {
    return (
      <Host class={ classNames({ visible: this.amIVisible() }) }>
        { this.amIVisible(this.allowFab) && (
          <div class="lobby">
            <button
              onClick={ () => { this.openInternal(); } }
              onMouseEnter={ () => { this.isTooltipShownHover = true; } }
              onMouseLeave={ () => { this.isTooltipShownHover = false; } }
            ><SvgQuestionMark/></button>
            <mb-tooltip-advanced
              show={ this.isTooltipShownHover || this.isTooltipShownTimer }
              arrowPosition={ 'arrow-down-right' }
              message={ this.texts.tooltip }
            />
          </div>
        ) }
        <mb-overlay visible={ this.amIVisible(this.isModalShown) }>
          <mb-modal
            visible={ this.amIVisible(this.isModalShown) }
            centered={ true }
            elevated={ true }
            hideFooter={ true }
            hideCloseButton={ this.activeStepIndex === 0 }
            onClose={ (event: CustomEvent) => {
              event.stopPropagation();
              this.closeInternal()
            } }
          >
            <div class="modal-content" slot="content">
              <div class="svg-container">
                { this.activeStepIndex === 0 && (<SvgCardNumberPosition/>) }
                { this.activeStepIndex === 1 && (<SvgValidDocument/>) }
                { this.activeStepIndex === 2 && (<SvgInvalidDocumentInvisibleFields/>) }
                { this.activeStepIndex === 3 && (<SvgInvalidDocumentHarshLight/>) }
                { this.activeStepIndex === 4 && (<SvgInvalidDocumentToMuchMotion/>) }
              </div>
              <p class="title">{ this.texts.steps[this.activeStepIndex].title }</p>
              <p class="description">{ this.texts.steps[this.activeStepIndex].description }</p>
              { this.activeStepIndex > 0 && (
                <div class="progress-tracker-container">
                  <mb-progress-tracker size={4} current={ this.activeStepIndex } />
                </div>
              ) }
              { this.activeStepIndex === 0 && (
                <div class="buttons onboarding">
                  <mb-button-classic clickHandler={ () => { this.closeInternal() }}>
                    { this.texts.steps[this.activeStepIndex].buttonNext }
                  </mb-button-classic>
                </div>
              ) }
              { this.activeStepIndex === 1 && (
                <div class="buttons">
                  <mb-button-classic inverted={ true } disabled={ true } clickHandler={ undefined }>
                    { this.texts.steps[this.activeStepIndex].buttonBack }
                  </mb-button-classic>
                  <mb-button-classic
                    clickHandler={ () => { this.activeStepIndex++; }}
                  >
                  { this.texts.steps[this.activeStepIndex].buttonNext }
                  </mb-button-classic>
                </div>
              ) }
              { (this.activeStepIndex === 2 || this.activeStepIndex === 3) && (
                <div class="buttons">
                  <mb-button-classic inverted={ true } clickHandler={ () => { this.activeStepIndex--; } }>
                    { this.texts.steps[this.activeStepIndex].buttonBack }
                  </mb-button-classic>
                  <mb-button-classic
                    clickHandler={ () => { this.activeStepIndex++; }}
                  >
                    { this.texts.steps[this.activeStepIndex].buttonNext }
                  </mb-button-classic>
                </div>
              ) }
              { this.activeStepIndex === 4 && (
                <div class="buttons">
                  <mb-button-classic inverted={ true } clickHandler={ () => { this.activeStepIndex--; } }>
                  { this.texts.steps[this.activeStepIndex].buttonBack }
                  </mb-button-classic>
                  <mb-button-classic clickHandler={ () => { this.closeInternal(); } }>
                    { this.texts.steps[this.activeStepIndex].buttonNext }
                  </mb-button-classic>
                </div>
              ) }
            </div>
          </mb-modal>
        </mb-overlay>
      </Host>
    );
  }

  private prepareTexts = () => {
    this.texts = {
      tooltip: this.translationService.i('help-button-lobby-tooltip') as string,
      steps: [
        {
          title: this.translationService.i('help-doc-valid-title') as string,
          description: this.translationService.i('help-doc-valid-description') as string,
          buttonNext: this.translationService.i('help-button-start-scanning') as string,
        },
        {
          title: this.translationService.i('help-doc-valid-card-number-field-title') as string,
          description: this.translationService.i('help-doc-valid-card-number-field-description') as string,
          buttonBack: this.translationService.i('help-button-back') as string,
          buttonNext: this.translationService.i('help-button-next') as string,
        },
        {
          title: this.translationService.i('help-doc-invalid-invisible-fields-title') as string,
          description: this.translationService.i('help-doc-invalid-invisible-fields-description') as string,
          buttonBack: this.translationService.i('help-button-back') as string,
          buttonNext: this.translationService.i('help-button-next') as string,
        },
        {
          title: this.translationService.i('help-doc-invalid-harsh-light-title') as string,
          description: this.translationService.i('help-doc-invalid-harsh-light-description') as string,
          buttonBack: this.translationService.i('help-button-back') as string,
          buttonNext: this.translationService.i('help-button-next') as string,
        },
        {
          title: this.translationService.i('help-doc-invalid-to-much-motion-title') as string,
          description: this.translationService.i('help-doc-invalid-to-much-motion-description') as string,
          buttonBack: this.translationService.i('help-button-back') as string,
          buttonNext: this.translationService.i('help-button-done') as string,
        },
      ]
    };
  };
}

interface StepTexts {
  title: string;
  description: string;
  buttonNext: string;
  buttonBack?: string;
}

interface ComponentTexts {
  tooltip: string;
  steps: StepTexts[];
}

